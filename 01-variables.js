// var
var helloWorld = "Hello World!"; // This variable is global
console.log(helloWorld);

// let 
let helloWorld2 = "Hello World!"; // This variable can only be access within the same block
console.log(helloWorld2);

// const
const helloWorld3 = "Hello World!"; // This variable can only access within the same block and cannot be re-assigned
console.log(helloWorld3);